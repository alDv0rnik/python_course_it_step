"""
Создайте список, состоящий из N элементов, которые указываются функцией ввода данных.
Первым вводом укажи размер списка (N), далее — элементы списка. Следует найти и вывести среднее арифметическое чисел
 данного списка.
"""
skor = int(input())
mid = []
for i in range(skor):
    num = int(input())
    mid.append(num)
print("List:", mid)
print( "sum:", sum(mid) / 2)
